function str = strrepbatch_operator(str)
yxops = [" .^ ", ".^";
    " ^ ", "^";
    " .^ ", ". ^";
    " ./ ", "./";
    " .\ ", ".\";
    " + ", "+";
    " ~ ", "~";
    " - ", "-";
    " * ",  "*";
    " / ", "/";
    " \ ", "\";
    " ./ ", ". /";
    " .\ ", ". \";
    "( " , "(";
    " )" , ")";
    " : " , ":";
    " >= ", ">=";
    " == ", "==";
    " <= ", "<=";
    " > ", ">";
    " < ", "<";
    " = ", "=";
    " >= ", ">  =";
    " == ", "=  =";
    " <= ", "<  =";
    " && ", "&&";
    " || ", "||";
    " & ", "&";
    " | ", "|";
    " && ", "&  &";
    " || ", "|  |";
    ];
    % yxops = cellstr([strop,strtrim(strop)]);
    % yxops = strop;
    str = strrepbatch(str, yxops);
end